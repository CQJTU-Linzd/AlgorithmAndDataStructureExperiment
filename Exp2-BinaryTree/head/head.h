#include<iostream>
using namespace std;
#include<vector>
#include<stack>
#include<queue>

class TreeNode {
public:
    int val;
    TreeNode* left;
    TreeNode* right;
    TreeNode() {
        this->left = NULL;
        this->right = NULL;
    }
    TreeNode(int val) {
        this->val = val;
        this->left = left;
        this->right = right;
    }
};